import { promises as fs } from 'node:fs';
import path from 'node:path';

const ROOT = path.resolve('public/Assets/Vehicles/Player');
const QUALITY_DIRS = [
  ['hd', 'HD'],
  ['pixel', 'Pixel'],
];
const OUT_FILE = path.resolve('src/data/vehicles.generated.js');

function parseVehicleId(filename) {
  const match = filename.match(/^Vehicles_(.+?)_[^/]+\.png$/i);
  if (!match) return null;
  return String(match[1]).toUpperCase();
}

async function listPngs(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith('.png'))
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

async function main() {
  const variantsByVehicle = new Map();

  for (const [qualityKey, qualityDir] of QUALITY_DIRS) {
    const dir = path.join(ROOT, qualityDir);
    const files = await listPngs(dir);
    for (const file of files) {
      const id = parseVehicleId(file);
      if (!id) continue;
      const next = variantsByVehicle.get(id) || {};
      next[qualityKey] = `/Assets/Vehicles/Player/${qualityDir}/${file}`;
      variantsByVehicle.set(id, next);
    }
  }

  const sorted = Object.fromEntries(
    [...variantsByVehicle.entries()]
      .sort((a, b) => a[0].localeCompare(b[0]))
      .map(([id, variants]) => [id, variants])
  );

  const lines = [
    '// Generated by scripts/generate-vehicle-catalog.mjs',
    'export const GENERATED_VEHICLE_VARIANTS = ' + JSON.stringify(sorted, null, 2) + ';',
    '',
  ];
  await fs.writeFile(OUT_FILE, lines.join('\n'), 'utf8');

  for (const [id, variants] of Object.entries(sorted)) {
    const missing = QUALITY_DIRS.map(([key]) => key).filter((key) => !variants[key]);
    console.log(`${id}: ${missing.length ? `missing ${missing.join(', ')}` : 'ok'}`);
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
